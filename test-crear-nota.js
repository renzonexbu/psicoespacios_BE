// Funci√≥n para obtener el ID del psic√≥logo desde el token
function getUserIdFromToken(token) {
  try {
    const payload = JSON.parse(Buffer.from(token.split('.')[1], 'base64').toString());
    return payload.sub; // sub contiene el ID del usuario
  } catch (error) {
    console.error('‚ùå Error decodificando token:', error.message);
    return null;
  }
}

// Funci√≥n para obtener un paciente existente (para usar en las pruebas)
async function getPacienteExistente(token) {
  try {
    // Intentar obtener pacientes del psic√≥logo logueado
    const userId = getUserIdFromToken(token);
    if (!userId) {
      console.log('‚ùå No se pudo obtener el ID del usuario del token');
      return null;
    }

    const response = await axios.get(`${BASE_URL}/api/v1/psicologos/${userId}/pacientes`, {
      headers: { Authorization: `Bearer ${token}` }
    });

    if (response.data.length > 0) {
      return response.data[0].pacienteId;
    } else {
      console.log('‚ö†Ô∏è No hay pacientes asignados, usando ID de ejemplo');
      // Usar un ID de ejemplo si no hay pacientes
      return '00000000-0000-0000-0000-000000000001';
    }
  } catch (error) {
    console.log('‚ö†Ô∏è No se pudo obtener paciente existente, usando ID de ejemplo');
    return '00000000-0000-0000-0000-000000000001';
  }
}

