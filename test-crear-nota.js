// Test 4: Crear nota con diferentes tipos
async function testCrearNotaDiferentesTipos() {
  try {
    console.log('\n--- Test 4: Crear notas con diferentes tipos ---');
    
    const pacienteId = await getPacienteExistente(psicologoToken);
    if (!pacienteId) {
      console.log('‚ùå No se pudo obtener un paciente para la prueba');
      return false;
    }

    const tiposNota = [
      { tipo: 'sesion', contenido: 'Nota de sesi√≥n terap√©utica.' },
      { tipo: 'evaluacion', contenido: 'Evaluaci√≥n del estado actual del paciente.' },
      { tipo: 'plan_tratamiento', contenido: 'Plan de tratamiento propuesto.' },
      { tipo: 'progreso', contenido: 'Nota de progreso del paciente.' },
      { tipo: 'otro', contenido: 'Nota de tipo otro.' }
    ];

    let notasCreadas = 0;
    for (const tipoNota of tiposNota) {
      try {
        const notaData = {
          pacienteId: pacienteId,
          contenido: tipoNota.contenido,
          titulo: `Nota de ${tipoNota.tipo}`,
          tipo: tipoNota.tipo
        };

        const response = await axios.post(`${BASE_URL}/api/v1/notas`, notaData, {
          headers: { Authorization: `Bearer ${psicologoToken}` }
        });

        console.log(`‚úÖ Nota tipo '${tipoNota.tipo}' creada: ${response.data.id}`);
        notasCreadas++;
      } catch (error) {
        console.log(`‚ö†Ô∏è Error creando nota tipo '${tipoNota.tipo}': ${error.response?.data?.message || error.message}`);
      }
    }

    console.log(`üìä Total de notas creadas: ${notasCreadas}/${tiposNota.length}`);
    return notasCreadas === tiposNota.length;
  } catch (error) {
    console.error('‚ùå Error en test de diferentes tipos:', error.message);
    return false;
  }
}

